package dk.ryan.danskverber500;

public class Verb {
private long id;
private String translation;
private String navnem;
private String nutid;
private String datid;
private String førnutid;
private String førdatid;
private String bydem;
private String boj;
public long getId() {
	return id;
}
public void setId(long id) {
	this.id = id;
}
public String getTranslation() {
	return translation;
}
public void setTranslation(String translation) {
	this.translation = translation;
}
public String getNavnem() {
	return navnem;
}
public void setNavnem(String navnem) {
	this.navnem = navnem;
}
public String getNutid() {
	return nutid;
}
public void setNutid(String nutid) {
	this.nutid = nutid;
}
public String getDatid() {
	return datid;
}
public void setDatid(String datid) {
	this.datid = datid;
}
public String getFørnutid() {
	return førnutid;
}
public void setFørnutid(String førnutid) {
	this.førnutid = førnutid;
}
public String getFørdatid() {
	return førdatid;
}
public void setFørdatid(String førdatid) {
	this.førdatid = førdatid;
}
public String getBydem() {
	return bydem;
}
public void setBydem(String bydem) {
	this.bydem = bydem;
}
public String getBoj() {
	return boj;
}
public void setBoj(String boj) {
	this.boj = boj;
}


}
